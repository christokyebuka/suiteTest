package Project2021;

public class YearlyEarnings {
    double jan_income;
    double feb_income;
    double march_income;
    double april_income;

    public double getJan_income() {
        return jan_income;
    }

    public void setJan_income(double jan_income) {
        this.jan_income = jan_income;
    }

    public double getFeb_income() {
        return feb_income;
    }

    public void setFeb_income(double feb_income) {
        this.feb_income = feb_income;
    }

    public double getMarch_income() {
        return march_income;
    }

    public void setMarch_income(double march_income) {
        this.march_income = march_income;
    }

    public double getApril_income() {
        return april_income;
    }

    public void setApril_income(double april_income) {
        this.april_income = april_income;
    }
}
